<p>El Partido del Trabajo respalda la aplicación del Impuesto Especial Sobre Producción y Servicios (IEPS) a las bebidas azucaradas y al tabaco, los llamados impuestos saludables, al considerar que enfermedades como la diabetes y el cáncer de pulmón, se han convertido en graves problemas de salud pública que generan un alto índice de mortandad entre la población. </p>
<p>Asimismo, apoya la propuesta para fijar un gravamen del 8% a los videojuegos que exaltan la violencia, ya que como sociedad se debe fomentar otro tipo de diversión entre nuestras niñas, niños y adolescentes, quienes son el futuro de México y se debe evitar la promoción de conductas antisociales y el uso de armas. </p>
