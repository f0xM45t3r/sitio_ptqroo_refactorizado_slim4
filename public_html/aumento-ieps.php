<p>Reconoce que el proyecto fue elaborado con perspectiva humanista y como parte del gobierno que encabeza nuestra Presidenta Claudia Sheinbaum apoya el concepto de prosperidad compartida, el cual abonará a la consolidación del segundo piso de la Cuarta Transformación. </p>
<p>Por ello, el Partido del Trabajo expresa su disposición por seguir abanderando las causas más sentidas de la población para honrar la premisa: Por el Bien de Todos, Primero los Pobres. </p>

